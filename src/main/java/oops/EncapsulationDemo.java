package oops;

public class EncapsulationDemo {

    private int a;

    private String s1;

    public void setA(int b){
        a=b;
    }

    public int getA(){
        return a;
    }

    public void setS1(String s2){
        s1=s2;
    }

    public String getS1(){
        return s1;
    }

}
